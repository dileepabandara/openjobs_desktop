    public ArrayList<Projects> projectList(){
        ArrayList<Projects> projectList = new ArrayList<>();
        try {
            Connection conn = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/openjobs", "root", "");
            String query = "SELECT * FROM project";
            Statement st = conn.createStatement ();
            ResultSet rs = st.executeQuery(query);       
            Projects projects;
             while (rs.next()) {                
                projects = new Projects(rs.getString("pID"), rs.getString("pName"),rs.getInt("pDays"));
                projectList.add(projects);
            }
        } catch (Exception e) {
        }
        return projectList;
    }
    
    public void show_projects(){
    
        ArrayList<Projects> list = projectList();
        DefaultTableModel model = (DefaultTableModel) jTable_Display_Projects.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getpID();
            row[1] = list.get(i).getpName();
            row[2] = list.get(i).getpDays();
            model.addRow(row);
        }
    
    }
    
