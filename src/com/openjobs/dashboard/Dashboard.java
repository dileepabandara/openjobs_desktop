    private void btnUpdate_ReceiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate_ReceiveActionPerformed
      
        cmbUpdatePTW = cmbPTW_Receive.getSelectedItem().toString();
        txtUpdatePTW = txtPTW_Receive.getText();
        
        String pName = cmbUpdatePTW;
        int pWeight = Integer.parseInt(txtUpdatePTW); 
        
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogButton2 = JOptionPane.ERROR_MESSAGE;
      
        if (txtUpdatePTW.length()>2 || txtUpdatePTW.length()==0){
             getToolkit().beep();
             JOptionPane.showMessageDialog(this, "Please enter the value between the valid range\nRange 1-99", "Out of Range", dialogButton2);
        } 
        else if (txtUpdatePTW.equals("")){
             getToolkit().beep();
             JOptionPane.showMessageDialog(this, "Please enter value", "Error", dialogButton2);
        }
        else if (txtUpdatePTW.equals("0")){
             getToolkit().beep();
             JOptionPane.showMessageDialog(this, "Value cannot be 0", "Error", dialogButton2);
        }
        else{
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to update?", "Update Weight", dialogButton);
            if(dialogResult == 0) {
                new UpdateDataToDB().update_tbl_path_weighted(pName, pWeight);   
            } else {}
        }
    }//GEN-LAST:event_btnUpdate_ReceiveActionPerformed

