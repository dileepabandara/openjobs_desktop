    private void lbl_more_optionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_more_optionsMouseClicked
        JDialog.setDefaultLookAndFeelDecorated(true);
        Object[] selectionValues = { "Create Database", "Change Credentials", "Reset Data" };
        String initialSelection = "Create Database";
        Object selection = JOptionPane.showInputDialog(null, "Please select an option",
            "Select Option", JOptionPane.QUESTION_MESSAGE, null, selectionValues, initialSelection);
        
        if ("Create Database".equals(selection.toString())) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to create database?",  "" +selection, dialogButton);
            if(dialogResult == 0) {
                JOptionPane.showMessageDialog(this, "Checking database to create...",  "" +selection, JOptionPane.INFORMATION_MESSAGE);
                new CreateDB().startCreateDB();
        } else {
                JOptionPane.showMessageDialog(this, "Process Terminated",  "" +selection, JOptionPane.INFORMATION_MESSAGE);}
        } 
        else if ("Change Credentials".equals(selection.toString())) {
            JOptionPane.showMessageDialog(this, "Please use phpMyAdmin panel ",  "" +selection,  JOptionPane.INFORMATION_MESSAGE);
        }
        else if("Reset Data".equals(selection.toString())) {
            JOptionPane.showMessageDialog(this, "This option is being updating.. please use phpMyAdmin to reset the data", "" +selection, JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(this, "Thank You", "Done ", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_lbl_more_optionsMouseClicked

    public void setTransferData(){
    
         int[] values = new int[10];
        JFileChooser chooser = new JFileChooser();
         chooser.showOpenDialog(null);
         File f = chooser.getSelectedFile();
         String filename = f.getAbsolutePath();
         
         try {
            FileReader reader = new FileReader(filename);
            BufferedReader br = new BufferedReader(reader);
            txt_read_locations_transfer.read(br,null);
            br.close();
            txt_read_locations_transfer.requestFocus();
            
            String input = txt_read_locations_transfer.getText();
            values = new int[input.length()];
            for(int i = 0; i < input.length(); i++) {
                values[i] = Integer.parseInt(input.substring(i, i+1));
            }
    
            txt_found_locations_transfer.setText(Arrays.toString(values));
            System.out.println(Arrays.toString(values));
            new MergeSort().runMergeSort(values);
            int finalArr[] = new MergeSort().finalArr;
            txt_transfered_locations_transfer.setText(Arrays.toString(finalArr));
            
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    
    }
    
